<h1>Shadowclan Rules</h1>

<p>The point of being in Shadowclan is to have more fun than one would have in another guild.
Thus the intent of these rules is to help us all have more fun through acting in a way that respects others.
For those familiar with the spirit of Shadowclan, most of these rules are common sense.</p>

<p>
The most current set of rules can be found in the <a href="http://www.shadowclan.org/darkmoot/viewtopic.php?t=35647">Comprehensive Rules and Regulations post</a> on our Message boards.
</p>

<h2>Handling problems</h2>

<h3>In-game reaction</h3>
<p>Some problems can be handled by leaders in-game in-character. Some problems, however, have no appropriate in-character response.  </p>

<p>
<h3>Reporting issues</h3>
<p>
Contact a clan leader privately in-game or <a href="http://www.shadowclan.org/darkmoot/groupcp.php?g=4131">out-of-game</a> and present a detailed report of the incident.
</p>
<h3>How reports are handled</h3>
<p>
Leaders will address problems by trying to fix the problem and to make sure it doesn't happen again.  Sometimes this involves a verbal correction, sometimes a warning, and sometimes more serious punishment.
</p>


<h2>Roleplaying</h2>

<p>Shadowclan characters are in-character 100% of the time they are in-game.  Thus, characters that are logged-in are expected to be responsive and actively controlled by the player.</p>

<p>If you play more than one Shadowclan character, please do not expect one character to be treated differently because of the reputation of the other character.  Similarly, please treat each character as an individual character, regardless of who is playing the character.

<h2>Chain of command</h2>
<p>
The Shadowclan chain of command must be followed at all times.  At the same time, it is expected that authority not be abused.  Higher ranks can assume control of groups, and discipline members (using the duel feature).
</p>


<h2>Cooperation</h2>

<h3>Grouping</h3>
<p>
You are expected to preferably group with Shadowclan members.  Always look for other Shadowclan groups to join.  If already in a Shadowclan group, and a Shadowclan member of a similar level is looking for a group, do what you can to get them to join you.
 When in a Shadowclan group, listen and obey the Orc in charge of the group.  Be patient with the group leader and do not attempt to make leading the group more difficult.
</p>

<h3>Events</h3>
<p>
If you are in-game at the time of a scheduled clan-wide event, you must try to attend the event.  Not only are they fun, but they are a good way to see what Shadowclan is really all about.
</p>

<h3>Sharing</h3>
<p>
Equipment, supplies, and money are shared among Shadowclan members. Feel free to sell <i>grey</i> items and equipment.  <i>White</i> items should be given to a clan crafter, and <i>white</i> equipment (weapons and armor) handed down to other Shadowclan members in need or sold.
</p>

<h3>Crafting</h3>
<p>
Acquiring resource from other orcs for developing professions is not allowed without special permission, which you can only request once you become a Gruntee.  Developing at least one gathering profession is required until gruntee rank.
</p>


<h3>Respect other players</h3>
<p>
Although you are an orc that distrusts anyone outside of Shadowclan, don't act mean in a way that other players will take personally.
</p>
<p>
If you have problems with another member of Shadowclan, report the problem to a clan leader and let the clan deal with it.
</p>
<p>
Never break the terms of service for playing World of Warcraft.
</p>

<h2>Blahing</h2>

<p>
Your Shadowclan character is expected to always be in-character when in-game, and thus should be in-character on all chat channels.
The only exception is private player-to-player /tell communication may be out-of-character when necessary.

<p>Use /say as much as possible</p>

<h3>Guild chat</h3>

<p><b>Guild chat is for:</b> <br />
In-character communication only <br />
Saying "Ug" when you log on <br />
Saying "Gug'ye" when you log off <br />
Saying "Hoowah" or "Ug" to new Peons <br />
Cheering for new Gruntees <br />
<i>Brief</i> friendly banter with those not nearby  <br />
<i>Occasional</i> questions when other communication channels fail to get an answer <br />
Announcing <i>significant </i> in-character achievements <br />
Detailed reports of major enemy groups in areas with many clan members <br />
Detailed announcements of clan activities <br />
Crafter service announcements <br />
Sharing, i.e. seeing if anyone wants a good item that you no longer want<br />
</p>

<p><b>Guild chat is NOT for:</b> <br />
Anything out-of-character <br />
Running a group or raid or trip <br />
Asking if nobs are available <br />
ALL CAPS, unless you are a clan leader and it is an emergency <br />
</p>

<h3>Message Boards</h3>
<p>
Your message board name should be the same as your character's name. If you have several main characters that need to post, we prefer that you register each character.
</p><p>
Do not cross post. Post only once, in the most appropriate forum.
</p><p>
No personal attacks or flaming. Be respectful of fellow players both in and out of Shadowclan.
</p><p>
When posting on a non-Shadowclan message board as a Shadowclan member, you are also expected to demonstrate maturity and show respect for others.  When answering questions about Shadowclan policy, check with a leader first or simply copy and paste the appropriate sentence from this website.
</p>

