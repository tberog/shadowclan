<h3>Tips</h3>  

<p>When speaking, keep things simple and to the point.</p> 
<p>Emphasize letters O, U and K.</p>
<p>When confused, just say "Me nub gruk".  </p>
<p>Don't forget to speak out loud to everyone when speaking, i.e. use "/say".</p>
<p>Feel free to use the in-game verbal commands, but <b>only</b> when appropriate.</p>
<p>Make the extra effort to ensure that what you are saying is going into the proper chat channel. If you do accidentally say something in the wrong chat channel, it is sometimes appropriate to follow it up with a simple "Uupz!" or "Bah!" to indicate that it is to be ignored.</p> 

<h3>Do's and Don'ts</h3>  
<p>Don't use smiley faces, use "Har!"</p>
<p>Don't use abbreviations like <i>rofl</i>. Spell out the whole word or phrase.</p>
<p>Don't use numerical symbols (1,5,20,etc).</p>
<p>Don't talk about things like levels, skill numbers, etc, except in a way that would be in-character for your character (i.e., learning more about something or getting stronger).</p>






