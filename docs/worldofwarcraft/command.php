<h1>Chain of Command</h1>
<p>
Shadowclan Orcs are expected to obey <b>all</b> orders from higher ranking Shadowclan Orcs.  The ranks, from lowest to highest, are:
</p>

<h3>Peons</h3>
<img src="images/peon.gif" alt="Peon" />
<p>
The lowest station amongst those in Shadowclan. Inferior in all skills of worth, these dogs are relegated to menial tasks such as mining. Downtrodden, the Orc Peons slave thanklessly to please their overseers with the hope that someday they will prove themselves worthy enough to become a Gruntee.
</p>
<p>
You can identify peons by their lack of shoulder armor or shirt. They don't show their helm and cloak. Peons do not ride anything but slow wolves.
</p>


<h3>Gruntees</h3>
<img src="images/grunt.gif" alt="Gruntee" />
<p>
Gruntees have proven themselves worthy of some respect and can be given the responsibility to oversee a group of Peons.  A Gruntee still has yet to prove to a Tribal Nob that he or she is worthy of belonging to a Tribe.
</p>
<p>
You can identify gruntees by the fact that they always wear shoulder armor, but no shirt and don't show their helm and cloak.
</p>

<h3>Grunts</h3>
<img src="images/wolfrider.gif" alt="Grunt" />
<p>
Grunts are powerful Orcs that have successfully joined a tribe.  They form the backbone of the clan.
</p>
<p>
You can identify a Grunt, and which Tribe they belong to, by their shirts color.
</p>

<h3>Nobs</h3>

<p>
Nobs are the highest ranking Orcs in the clan.  They lead their Tribes and the Clan, both in and out of game.
</p>
<p>
You can identify a Nob by their natural, powerful presence.
</p>

<h2>Other ranks of importance</h2>
<h3>Warboss</h3>
<p>
Every month, a member from each Tribe competes for the title of Warboss and the responsibility to lead the clan in battle.
</p>

