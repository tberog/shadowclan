 <h1>Expectations of New Members</h1>
<p>
New members start at the Peon rank. There are a few Peon rules to follow dealing with chain of command, rank identification, and professions.
</p>

<p>
<strong>Chain of command:</strong> Peons are at the lowest rung on Shadowclan's <a href="command.php">chain of command</a>.  Thus, Peons must follow the orders of anyone else in Shadowclan, with the exception of other Peons.
</p>

<p>
<strong>Equipment:</strong> Peons are not allowed to wear shirts or shoulder armor, they also do not show a helm or cloak.  All shoulder equipment is reserved for Gruntee rank and higher. Peons are also not allowed to either purchase or ride fast mounts. You will be given a mount when you become Gruntee.
</p>
<p>
<strong>Professions:</strong> Peons like to gather materials. While you are a Peon, you are expected to develop one or more of the following gathering skills: skinning, mining, herbalism. In addition, first aid is a recommended skill. You can take up other professions too, but you can not use materials collected by other clan members until you receive special permission which is only granted to gruntees.
</p>
 <h2>Additional Expectations</h2>
<p>
In addition to following the above rules, Peons should focus on learning about Shadowclan and make the effort to act in the way expected of Shadowclan members.  Specifically, focus on:
</p>
<h3>Roleplaying</h3>
<p>
Always be in-character while in-game.  Learn and use the Shadowclan orc <a href="language.php">basic language</a>.
</p>
<p>
Behave like a Peon.  Accept duels from other Shadowclan members.</p>
<p>
Develop an interesting roleplay persona that you assume when you play your Shadowclan character. What is your orc character like?  Fearless or fearful? Bloodthirsty or pacifist? Old or young? Drunk or drunker? This is fun for you, and fun for others who meet you.</p>


<h3>Cooperation</h3>

<p>
When grouping you are expected to group preferably with Shadowclan members.  When in a Shadowclan group, listen and obey the Orc in charge of the group.
</p>
<p>
If you are in-game at the time of a scheduled clan-wide event, you must try to attend the event.  Not only are they fun, but they are a good way to see what Shadowclan is really all about.</p>
<p>
Equipment, supplies, and money are shared among Shadowclan members. Feel free to sell <i>grey</i> items and equipment.  <i>White</i> items should be given to a clan crafter, and <i>white</i> equipment (weapons and armor) handed down to other Shadowclan members in need or sold. A <i>green</i> or <i>blue</i> item (not used by you or someone else in the group that finds it) should be given to the Goth of Commerce. Any excess coins should be given to tribe leaders, especially the ones in charge of the tribe you are most interested in joining.</p>

<h3>Respect other players</h3>
<p>
Although you are an orc that distrusts anyone outside of Shadowclan, don't act mean in a way that other players will take personally.
</p>
<p>
If you have problems with another member of Shadowclan, report the problem to a clan leader and let the clan deal with it.
</p>
<p>
Never break the terms of service for playing World of Warcraft.
</p>


<h3>Proper use of chat</h3>
<p>
Use /say as often as possible for communication. Use /tell, /party, /guild only when /say is out of range.  All communication must be in-character, with the only exception being /tell when necessary or appropriate.
</p>

<h3>Learn more about Shadowclan</h3>
<p>
Use your time as a Peon to learn more about Shadowclan and its Tribes. Read through this website. Start checking the message boards about as often as you log in your character so that you can stay up to date with scheduled events and changes to the game.
</p>

<h3>Seek out Gruntee rank</h3>
<p>
Assuming you enjoy being part of Shadowclan, you will want to become a Gruntee as soon as possible (certainly before level 30).  Learn more about <a href="gruntee.php">becoming a Gruntee</a>.
</p>

