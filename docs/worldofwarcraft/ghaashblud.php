<h1>Ghaashblud Tribe</h1>

<p>
In the early days of the war with the humans, Shadowclan fought like most of the other Orcs. However, as the years of the war waged on a certain uncontrollable rage started growing within the Clan. In order to harness that violent lust for blood and battle the Ghaashblud Tribe was founded. It was ever the intent of the Clan Elders to disband the Tribe if peace ever came, but it never has. 
</p>


<h2>Description</h2>
<p>
The leaders of the Ghaashblud Tribe know that every arm and every mind can be useful in battle, and so in the Tribe there is a unholy alliance of sorts between the shamans and the warlocks. Each put aside their philosophy and hatred for each other’s ways and have found unity in one purpose - to spill the red blood of the humans.
</p>
<p>
Battle is life. The tribe lives to fight wars, and fights to live. Members of the tribe are expected to be fearless, willing to embrace death as quickly as they would their blade in their never-ending desire to grind the bones of humans under their boots.
</p>
<p>
Fire plays a special role in the Ghaashblud tribe. It is said that when an Orc joins the tribe their very blood becomes infused with a mix of unholy and elemental fire. Fire also is where the Shamans of the Tribe see visions, and where the Demonic powers of the Warlocks come from. 
</p>

